							<h5>Increased Mortality in Elderly Patients with Dementia-Related Psychosis</h5>
							<p><strong>Elderly patients with dementia-related psychosis treated with antipsychotic drugs are at an increased risk (1.6 to 1.7 times) of death compared to placebo (4.5% vs 2.6%, respectively). Although the causes of death were varied, most of the deaths appeared to be cardiovascular (eg, heart failure, sudden death) or infectious (eg, pneumonia) in nature. ABILIFY is not approved for the treatment of patients with dementia-related psychosis.</strong></p>
							<h5>Contraindications</h5>
							<p>ABILIFY is contraindicated in patients who have known hypersensitivity reactions to ABILIFY. Reactions have ranged from pruritus/urticaria to anaphylaxis.</p>
