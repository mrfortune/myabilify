					<h5>Important Warning and Precaution for Tardive Dyskinesia (TD)</h5>
					<p>The risk of developing TD and the potential for it to become irreversible are believed to increase as the duration of treatment and the total cumulative dose of antipsychotic increase. The syndrome can develop, although much less commonly, after relatively brief treatment periods at low doses. Prescribing should be consistent with the need to minimize TD. The syndrome may remit, partially or completely, if antipsychotic treatment is withdrawn.</p>
					<h5>Important Warning and Precaution Regarding Metabolic Changes</h5>
					<p>Atypical antipsychotic drugs have been associated with metabolic changes that include hyperglycemia/diabetes mellitus, dyslipidemia, and body weight gain.</p>
					<h5>Important Warning and Precaution for Cognitive and Motor Impairment</h5>
					<p>Like other antipsychotics, ABILIFY may have the potential to impair judgment, thinking, or motor skills. Patients should not drive or operate hazardous machinery until they are certain ABILIFY does not affect them adversely.	</p>
					<h5>Important Warning and Precaution for Seizures/Convulsions</h5>
					<p>As with other antipsychotic drugs, ABILIFY should be used with caution in patients with a history of seizures or with conditions that lower the seizure threshold (eg, Alzheimer’s dementia).</p>
